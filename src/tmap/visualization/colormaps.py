def get_colormap(name: str) -> list[str]:
    """
    Get colormap as list of hex colors.

    Args:
        name: Colormap name (e.g., "viridis", "tab10")

    Returns:
        List of hex color strings

    Raises:
        ValueError: If colormap name is unknown
    """
    if name not in COLORMAPS:
        available = ", ".join(sorted(COLORMAPS.keys()))
        raise ValueError(f"Unknown colormap: {name}. Available: {available}")
    return COLORMAPS[name]


def map_continuous(
    values: NDArray[np.floating[Any]],
    colormap: str = "viridis",
) -> list[str]:
    """
    Map continuous values in [0, 1] to colors.

    Args:
        values: Array of values in [0, 1] range
        colormap: Colormap name

    Returns:
        List of hex color strings, one per value
    """
    colors = get_colormap(colormap)
    n_colors = len(colors)

    # Clip to valid range and map to indices
    values = np.asarray(values)
    values = np.clip(values, 0.0, 1.0)
    indices = (values * (n_colors - 1)).astype(int)
    indices = np.clip(indices, 0, n_colors - 1)

    return [colors[i] for i in indices]


def map_categorical(
    values: NDArray[Any],
    colormap: str = "tab10",
) -> tuple[list[str], dict[str, str]]:
    """
    Map categorical values to colors.

    Args:
        values: Array of categorical values (strings, ints, etc.)
        colormap: Colormap name (should be categorical like "tab10")

    Returns:
        Tuple of:
        - List of hex color strings, one per value
        - Legend dict mapping category labels to colors
    """
    colors = get_colormap(colormap)
    n_colors = len(colors)

    # Get unique categories in order of first appearance
    seen: dict[Any, int] = {}
    for v in values:
        key = v if not isinstance(v, np.ndarray) else tuple(v)
        if key not in seen:
            seen[key] = len(seen)

    # Map each value to a color (cycle if more categories than colors)
    result = []
    for v in values:
        key = v if not isinstance(v, np.ndarray) else tuple(v)
        idx = seen[key] % n_colors
        result.append(colors[idx])

    # Build legend
    legend = {str(k): colors[i % n_colors] for k, i in seen.items()}

    return result, legend
